package com.animalshelter.data;


public abstract class AnimalShelter_Data {
	public static int animalSerialNumber;
	
	public String animalType(String type) {
		this.animalType(type);
		return type;	
	}
	
	public String animalName(String name) {
		this.animalName(name);
		return name;
	}
	
	public int animalAge(int age) {
		this.animalAge(age);
		return age;
	}
	
	public String animalSound(String sound) {
		this.animalSound(sound);
		return sound;
	}
	
	public String animalGender(String gender) {
		this.animalGender(gender);
		return gender;
	}
}
